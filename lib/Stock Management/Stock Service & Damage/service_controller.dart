  List<Map<String, dynamic>> _slice(List<Map<String, dynamic>> src, int page) {
    final start = (page - 1) * AppLayout.pageSize;
    if (start >= src.length) return [];
    return src.sublist(
      start,
      (start + AppLayout.pageSize).clamp(0, src.length),
    );
  }

